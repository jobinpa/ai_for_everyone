supportedResolutions = []

cam = cv2.VideoCapture(1)

for r in commonResolutions:
    print(f'{r[0]}x{r[1]}'.ljust(12, ' '), end='')

    cam.set(cv2.CAP_PROP_FRAME_WIDTH, r[0])
    cam.set(cv2.CAP_PROP_FRAME_HEIGHT, r[1])

    w = int(cam.get(cv2.CAP_PROP_FRAME_WIDTH))
    h = int(cam.get(cv2.CAP_PROP_FRAME_HEIGHT))

    if w == r[0] and h == r[1]:
        print('SUPPORTED')
        supportedResolutions.append(r)
    else:
        print('NOT SUPPORTED')

cam.release()

print()
print('=====SUPPORTED RESOLUTIONS=====')
for r in supportedResolutions:
    print(f'{r[0]}x{r[1]}')
